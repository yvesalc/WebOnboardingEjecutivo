package cl.com.ccla.onboarding.VO;

public class UsuarioVO {
    
	private String rol_id;
    private String use_vigente;
    private String use_sucursal;
    private String use_username;
    private String rol_descripcion;

    
    public String getRol_id() {
		return rol_id;
	}
	public void setRol_id(String rol_id) {
		this.rol_id = rol_id;
	}
	public String getUse_vigente() {
		return use_vigente;
	}
	public void setUse_vigente(String use_vigente) {
		this.use_vigente = use_vigente;
	}
	public String getUse_sucursal() {
		return use_sucursal;
	}
	public void setUse_sucursal(String use_sucursal) {
		this.use_sucursal = use_sucursal;
	}
	public String getUse_username() {
		return use_username;
	}
	public void setUse_username(String use_username) {
		this.use_username = use_username;
	}
	public String getRol_descripcion() {
		return rol_descripcion;
	}
	public void setRol_descripcion(String rol_descripcion) {
		this.rol_descripcion = rol_descripcion;
	}    
    
    
}
